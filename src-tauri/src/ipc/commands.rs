#[tauri::command]
#[specta::specta]
pub fn list_variants(
    handle: ProjectHandle,
    asset_path: String,
    state: State<'_, SharedState>,
) -> CoreResult<Vec<VariantKey>> {
    let app = state.read();
    let project = app
        .projects
        .get(&handle.id)
        .ok_or(CoreError::ProjectNotFound)?;

    let (namespace, block_name) = blockstate_id_from_asset_path(&asset_path)
        .ok_or_else(|| CoreError::InvalidInput("not a blockstate path".to_string()))?;

    let source = project.source.as_ref();
    let pack = pack_for_project(project);
    let mut cache = project.model_cache.lock().expect("cache poisoned");
    let registry = ModelRegistry::new(source, &mut cache, pack);
    let blockstate = registry.load_blockstate(&namespace, &block_name)?;
    Ok(list_variant_keys(&blockstate))
}

#[tauri::command]
#[specta::specta]
pub fn models_for_texture(
    handle: ProjectHandle,
    asset_path: String,
    state: State<'_, SharedState>,
) -> CoreResult<Vec<ModelRefInfo>> {
    let app = state.read();
    let project = app.projects.get(&handle.id).ok_or(CoreError::ProjectNotFound)?;

    let indexed = texture_index::models_for_texture_path(&project.texture_model_index, &asset_path);
    if !indexed.is_empty() {
        return Ok(indexed);
    }

    let texture_stem = texture_stem_from_entry_path(&asset_path);
    let source = project.source.as_ref();
    let pack = pack_for_project(project);
    let mut cache = project.model_cache.lock().expect("cache poisoned");
    let mut registry = ModelRegistry::new(source, &mut cache, pack);
    find_models_for_texture(&mut registry, &project.entries, &asset_path, &texture_stem)
}

#[tauri::command]
#[specta::specta]
pub fn resolve_renderable(
    handle: ProjectHandle,
    asset_path: String,
    variant_key: Option<String>,
    linked_model_path: Option<String>,
    state: State<'_, SharedState>,
) -> CoreResult<RenderableModel> {
    let app = state.read();
    let project = app.projects.get(&handle.id).ok_or(CoreError::ProjectNotFound)?;

    let resolve_path = linked_model_path.as_deref().unwrap_or(asset_path.as_str());
    let entry = project
        .entries
        .iter()
        .find(|e| e.path == resolve_path)
        .ok_or_else(|| CoreError::AssetNotFound(resolve_path.to_string()))?;

    let source = project.source.as_ref();
    let pack = pack_for_project(project);
    let mut cache = project.model_cache.lock().expect("cache poisoned");
    let mut registry = ModelRegistry::new(source, &mut cache, pack);

    match entry.kind {
        AssetKind::Texture => {
            let mut models =
                texture_index::models_for_texture_path(&project.texture_model_index, &asset_path);
            if models.is_empty() {
                let texture_stem = texture_stem_from_entry_path(&asset_path);
                models = find_models_for_texture(
                    &mut registry,
                    &project.entries,
                    &asset_path,
                    &texture_stem,
                )?;
            }
            if let Some(first) = models.first() {
                let (ns, path) = if let Some((a, b)) = first.model_id.split_once(':') {
                    (a.to_string(), b.to_string())
                } else {
                    (entry.namespace.clone(), first.model_id.clone())
                };
                let resolved = registry.resolve_model(&ns, &path)?;
                compile_renderable(&resolved, &ns, None, &pack, &registry)
            } else {
                compile_texture_preview(&asset_path, &registry)
            }
        }
        AssetKind::BlockModel | AssetKind::ItemModel => {
            let (ns, model_path) = model_id_from_asset_path(&asset_path)
                .ok_or_else(|| CoreError::InvalidInput("invalid model path".to_string()))?;
            let resolved = registry.resolve_model(&ns, &model_path)?;
            compile_renderable(&resolved, &ns, None, &pack, &registry)
        }
        AssetKind::Blockstate => {
            let (ns, block_name) = blockstate_id_from_asset_path(&asset_path)
                .ok_or_else(|| CoreError::InvalidInput("invalid blockstate path".to_string()))?;
            let blockstate = registry.load_blockstate(&ns, &block_name)?;

            if blockstate.multipart.is_some() && blockstate.variants.is_empty() {
                let state = variant_key
                    .as_ref()
                    .map(|key| parse_variant_state(key))
                    .unwrap_or_default();
                let variants = resolve_multipart_models(&blockstate, &state);
                if variants.is_empty() {
                    return Err(CoreError::InvalidInput(
                        "no multipart models matched".to_string(),
                    ));
                }
                return compile_multipart_renderable(&mut registry, &ns, &variants, &pack);
            }

            let variants = collect_variant_models(&blockstate);
            let (variant, _) = variants
                .into_iter()
                .find(|(_, key)| variant_key.as_ref().is_none_or(|vk| vk == key))
                .ok_or_else(|| CoreError::InvalidInput("no variants in blockstate".to_string()))?;
            let (m_ns, m_path) = normalize_model_ref(&variant.model, &ns);
            let resolved = registry.resolve_model(&m_ns, &m_path)?;
            compile_renderable(&resolved, &m_ns, Some(&variant), &pack, &registry)
        }
        _ => Err(CoreError::InvalidInput(
            "asset kind cannot be rendered".to_string(),
        )),
    }
}

#[tauri::command]
#[specta::specta]
pub fn read_recent_logs(max_lines: Option<u32>) -> Result<crate::dto::LogTailResponse, String> {
    let limit = max_lines.unwrap_or(200).clamp(1, 2000) as usize;
    let dir = crate::logging::log_directory()
        .ok_or_else(|| "log directory unavailable".to_string())?;

    let mut entries: Vec<_> = std::fs::read_dir(&dir)
        .map_err(|e| e.to_string())?
        .filter_map(|e| e.ok())
        .filter(|e| {
            e.file_name()
                .to_string_lossy()
                .starts_with("ind3x-art.log")
        })
        .collect();

    entries.sort_by_key(|b| std::cmp::Reverse(b.metadata().and_then(|m| m.modified()).ok()));

    let file_path = entries.first().map(|e| e.path());
    let lines = if let Some(ref path) = file_path {
        read_tail_lines(path, limit).unwrap_or_default()
    } else {
        vec![]
    };

    Ok(crate::dto::LogTailResponse {
        log_dir: Some(dir.to_string_lossy().into_owned()),
        file: file_path.map(|p| p.to_string_lossy().into_owned()),
        lines,
    })
}

fn read_tail_lines(path: &std::path::Path, limit: usize) -> std::io::Result<Vec<String>> {
    use std::fs::File;
    use std::io::{Read, Seek, SeekFrom};

    let mut file = File::open(path)?;
    let file_len = file.metadata()?.len();
    if file_len == 0 {
        return Ok(vec![]);
    }

    const CHUNK: u64 = 16 * 1024;
    let mut pos = file_len;
    let mut buf = Vec::new();

    while pos > 0 {
        let read_size = CHUNK.min(pos);
        pos -= read_size;
        file.seek(SeekFrom::Start(pos))?;
        let mut chunk = vec![0u8; read_size as usize];
        file.read_exact(&mut chunk)?;

        if pos > 0 {
            if let Some(idx) = chunk.iter().position(|&b| b == b'\n') {
                chunk = chunk[idx + 1..].to_vec();
            } else {
                chunk.clear();
            }
        }

        buf.splice(0..0, chunk);

        let complete_lines = buf.iter().filter(|&&b| b == b'\n').count();
        let partial = usize::from(!buf.is_empty() && !buf.ends_with(&[b'\n']));
        if complete_lines + partial >= limit || pos == 0 {
            break;
        }
    }

    let text = String::from_utf8_lossy(&buf);
    let mut lines: Vec<String> = text.lines().map(str::to_string).collect();
    if lines.len() > limit {
        lines = lines.split_off(lines.len() - limit);
    }
    Ok(lines)
}

#[cfg(test)]
mod log_tail_tests {
    use super::read_tail_lines;
    use std::io::Write;

    #[test]
    fn read_tail_lines_returns_last_lines_without_loading_entire_file() {
        let dir = tempfile::tempdir().expect("tempdir");
        let path = dir.path().join("ind3x-art.log");
        let mut file = std::fs::File::create(&path).expect("create");
        for i in 0..500 {
            writeln!(file, "line-{i}").expect("write");
        }
        drop(file);

        let lines = read_tail_lines(&path, 3).expect("tail");
        assert_eq!(lines, vec!["line-497", "line-498", "line-499"]);
    }
}

#[tauri::command]
#[specta::specta]
pub fn reveal_log_dir(app: tauri::AppHandle) -> CoreResult<()> {
    let dir = crate::logging::log_directory()
        .or_else(|| app.path().app_log_dir().ok())
        .ok_or_else(|| CoreError::Unavailable("log directory unavailable".to_string()))?;

    std::fs::create_dir_all(&dir)?;

    app.opener()
        .open_path(dir.to_string_lossy().to_string(), None::<&str>)
        .map_err(|e| CoreError::Internal(e.to_string()))?;

    Ok(())
}
