fn pack_for_project(project: &crate::state::Project) -> PackInfo {
    PackInfo {
        pack_format: project.pack_format,
    }
}

fn rebuild_texture_model_index(project: &mut crate::state::Project) {
    let pack = pack_for_project(project);
    let entries = project.entries.clone();
    let mut cache = project.model_cache.lock().expect("cache poisoned");
    let mut registry = ModelRegistry::new(project.source.as_ref(), &mut cache, pack);
    project.texture_model_index =
        texture_index::build_texture_model_index(&mut registry, &entries);
    drop(cache);
    apply_texture_link_counts(project);
}

fn apply_texture_link_counts(project: &mut crate::state::Project) {
    let index = project.texture_model_index.clone();
    for entry in &mut project.entries {
        if entry.kind == AssetKind::Texture {
            let count =
                texture_index::models_for_texture_path(&index, &entry.path).len() as u32;
            entry.linked_model_count = Some(count);
        }
    }
}

