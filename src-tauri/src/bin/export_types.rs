/// Run with: cargo run --bin export_types
/// Generates src/ipc/generated.ts with TypeScript types derived from Rust DTOs.
use ind3x_art_lib::dto::{
    AppInfo, AssetEntry, AssetFacets, AssetFilter, AssetKind, AssetPage, BackupInfo,
    DisplayTransform, ElementRotation, FacetCount, IndexEvent, ModelRefInfo, ModelRotation,
    OpenSourceResult, PageReq, ProjectHandle, RenderCuboid, RenderFace, RenderableKind,
    RenderableModel, SaveJournalEntry, SaveMode, SaveOptions, SaveTexturesResult, SourceKind,
    TextureAnimationMeta, TextureMetaInfo, TexturePreview, TextureSaveEntry, VariantKey,
};

fn main() {
    use specta::TypeCollection;
    use specta_typescript::Typescript;

    let mut types = TypeCollection::default();
    types.register::<AppInfo>();
    types.register::<AssetKind>();
    types.register::<AssetEntry>();
    types.register::<ProjectHandle>();
    types.register::<OpenSourceResult>();
    types.register::<SourceKind>();
    types.register::<AssetFilter>();
    types.register::<FacetCount>();
    types.register::<AssetFacets>();
    types.register::<TexturePreview>();
    types.register::<PageReq>();
    types.register::<AssetPage>();
    types.register::<IndexEvent>();
    types.register::<RenderableKind>();
    types.register::<ModelRotation>();
    types.register::<ElementRotation>();
    types.register::<RenderFace>();
    types.register::<RenderCuboid>();
    types.register::<TextureAnimationMeta>();
    types.register::<TextureMetaInfo>();
    types.register::<DisplayTransform>();
    types.register::<RenderableModel>();
    types.register::<VariantKey>();
    types.register::<ModelRefInfo>();
    types.register::<TextureSaveEntry>();
    types.register::<SaveMode>();
    types.register::<SaveOptions>();
    types.register::<SaveJournalEntry>();
    types.register::<BackupInfo>();
    types.register::<SaveTexturesResult>();

    Typescript::default()
        .bigint(specta_typescript::BigIntExportBehavior::Number)
        .header("// AUTO-GENERATED by export_types.rs — do not edit manually.\n// Run:  cd src-tauri && cargo run --bin export_types")
        .export_to("../src/ipc/generated.ts", &types)
        .expect("failed to export types");

    println!("Types exported to src/ipc/generated.ts");
}
